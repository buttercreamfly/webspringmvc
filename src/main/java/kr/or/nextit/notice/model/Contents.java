package kr.or.nextit.notice.model;

public class Contents {

	private int no;
	private String noti_title;
	private String noti_auth;
	private String noti_core;
	private String noti_date;
	
	public Contents() {}

	public int getNo() {
		return no;
	}

	public void setNo(int no) {
		this.no = no;
	}

	public String getNoti_title() {
		return noti_title;
	}

	public void setNoti_title(String noti_title) {
		this.noti_title = noti_title;
	}

	public String getNoti_auth() {
		return noti_auth;
	}

	public void setNoti_auth(String noti_auth) {
		this.noti_auth = noti_auth;
	}

	public String getNoti_core() {
		return noti_core;
	}

	public void setNoti_core(String noti_core) {
		this.noti_core = noti_core;
	}

	public String getNoti_date() {
		return noti_date;
	}

	public void setNoti_date(String noti_date) {
		this.noti_date = noti_date;
	}

	
	
}
